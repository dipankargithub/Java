package Loop;

public class WhileLoop {
    public static void main(String[] args) {
        // Array object
        String[] names = {"Sagar", "Aditya", "Dipankar", "Riya", "Sumana", "nendu" };

        //While loop
        int i =0;
        while(i<6)
        {
            System.out.println(names[i]);
            i++;
        }
    }
}
